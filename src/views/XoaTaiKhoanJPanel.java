    private void btn_xoaTaiKhoanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaTaiKhoanActionPerformed
        String ma = edt_maSinhVien.getText().trim();
        if(jRadioGiaoVien.isSelected()) {
            if(GiaoVienController.getInstance().xoaGiaoVien(ma)) {
                JOptionPane.showMessageDialog(this, "Xóa thành công!","Thông báo", JOptionPane.INFORMATION_MESSAGE, new ImageIcon("src//images//icon_complete.png"));
            } else {
                JOptionPane.showMessageDialog(this, "Không tồn tại hoặc sai mã giáo viên!","Thông báo lỗi", JOptionPane.ERROR_MESSAGE);
            }
            hienThiGV();
        }
        else {
            if(SinhVienController.getInstance().xoaSinhVien(ma)) {
                JOptionPane.showMessageDialog(this, "Xóa thành công!","Thông báo", JOptionPane.INFORMATION_MESSAGE, new ImageIcon("src//images//icon_complete.png"));
            } else {
                JOptionPane.showMessageDialog(this, "Không tồn tại hoặc sai mã sinh viên!","Thông báo lỗi", JOptionPane.ERROR_MESSAGE);
            }
            hienThiSV();
        }
    }//GEN-LAST:event_btn_xoaTaiKhoanActionPerformed

