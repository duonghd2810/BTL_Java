    public void hienThiGV() {
        dsGiaoVien = GiaoVienController.getInstance().danhSachGiaoVien();
        model.setRowCount(0);
        for (GiaoVien giaoVien : dsGiaoVien) {
            model.addRow(new String[] {
                giaoVien.getMaGV(),
                giaoVien.getTenGV()
            });
        }
        model.fireTableDataChanged();
    }
    
    public void hienThiSV() {
        dsSinhVien = SinhVienController.getInstance().danhSachSinhVien();
        model.setRowCount(0);
        for (SinhVien sinhVien : dsSinhVien) {
            model.addRow(new String[] {
                sinhVien.getMaSV(),
                sinhVien.getTenSV()
            });
        }
        model.fireTableDataChanged();
    }
    
